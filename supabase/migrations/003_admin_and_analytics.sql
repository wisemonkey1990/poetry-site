create or replace function public.track_visitor_event(
  p_session_id uuid, p_path text, p_poem_id integer default null,
  p_referrer_host text default null, p_device_type text default 'desktop'
) returns void language plpgsql security definer set search_path = '' as $$
begin
  if p_session_id is null or p_path is null or left(p_path, 1) <> '/' then
    raise exception 'invalid analytics payload';
  end if;
  if p_device_type not in ('desktop', 'tablet', 'mobile') then
    raise exception 'invalid device type';
  end if;
  insert into public.visitor_events(event_type, page_path, session_id, poem_id, referrer_host, device_type)
  values ('page_view', left(p_path, 200), p_session_id, p_poem_id,
    left(nullif(p_referrer_host, ''), 120), p_device_type);
end $$;

create or replace function public.get_admin_dashboard(p_days integer default 7)
returns jsonb language plpgsql security definer set search_path = '' as $$
declare v_days integer := greatest(1, least(coalesce(p_days, 7), 365)); v_result jsonb;
begin
  if not public.is_admin() then raise exception 'permission denied'; end if;
  select jsonb_build_object(
    'pv', count(*),
    'uv', count(distinct session_id),
    'today_pv', count(*) filter (where created_at >= current_date),
    'today_uv', count(distinct session_id) filter (where created_at >= current_date),
    'hot_poems', coalesce((select jsonb_agg(x order by (x->>'views')::int desc) from (
      select jsonb_build_object('id', p.id, 'title', p.title, 'views', count(*), 'count', count(*)) x
      from public.visitor_events e join public.poems p on p.id=e.poem_id
      where e.created_at >= now() - make_interval(days => v_days)
      group by p.id, p.title order by count(*) desc limit 10) q), '[]'::jsonb),
    'sources', coalesce((select jsonb_agg(x order by (x->>'count')::int desc) from (
      select jsonb_build_object('name', coalesce(nullif(referrer_host,''),'direct'), 'count', count(*)) x
      from public.visitor_events where created_at >= now() - make_interval(days => v_days)
      group by coalesce(nullif(referrer_host,''),'direct') order by count(*) desc limit 12) q), '[]'::jsonb),
    'devices', coalesce((select jsonb_agg(x order by (x->>'count')::int desc) from (
      select jsonb_build_object('name', device_type, 'count', count(*)) x
      from public.visitor_events where created_at >= now() - make_interval(days => v_days)
      group by device_type order by count(*) desc) q), '[]'::jsonb)
  ) into v_result
  from public.visitor_events where created_at >= now() - make_interval(days => v_days);
  return v_result;
end $$;

create or replace function public.get_admin_timeseries(p_days integer default 7)
returns table(day date, pv bigint, uv bigint) language plpgsql security definer set search_path = '' as $$
declare v_days integer := greatest(1, least(coalesce(p_days, 7), 365));
begin
  if not public.is_admin() then raise exception 'permission denied'; end if;
  return query select d::date, count(e.id), count(distinct e.session_id)
  from generate_series(current_date - (v_days - 1), current_date, interval '1 day') d
  left join public.visitor_events e on e.created_at >= d and e.created_at < d + interval '1 day'
  group by d order by d;
end $$;

