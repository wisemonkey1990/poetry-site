-- 诗三百：后台管理、诗篇数据与匿名访问统计
create table if not exists public.poems (
  id integer generated by default as identity primary key,
  slug text unique,
  title text not null,
  chapter text not null,
  section text not null,
  content jsonb not null default '[]'::jsonb,
  annotation text,
  translation text,
  sort_order integer not null default 0,
  is_published boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.poems add column if not exists slug text;
alter table public.poems add column if not exists sort_order integer not null default 0;
alter table public.poems add column if not exists is_published boolean not null default true;
alter table public.poems add column if not exists created_at timestamptz not null default now();
alter table public.poems add column if not exists updated_at timestamptz not null default now();
update public.poems set slug = 'poem-' || id where slug is null or slug = '';
create unique index if not exists poems_slug_unique_idx on public.poems(slug);
create index if not exists poems_public_order_idx on public.poems(is_published, sort_order, id);

create table if not exists public.user_profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  nickname text not null,
  avatar_url text,
  bio text,
  status smallint not null default 1 check (status in (0, 1)),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.favorites (
  id bigint generated always as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  poem_id integer not null references public.poems(id) on delete cascade,
  created_at timestamptz not null default now(),
  unique(user_id, poem_id)
);
create index if not exists favorites_user_created_idx on public.favorites(user_id, created_at desc);

create or replace function public.handle_new_user()
returns trigger language plpgsql security definer set search_path = '' as $$
begin
  insert into public.user_profiles(id, nickname)
  values (new.id, coalesce(nullif(new.raw_user_meta_data->>'nickname', ''), split_part(new.email, '@', 1)))
  on conflict (id) do nothing;
  return new;
end $$;
drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created after insert on auth.users
for each row execute function public.handle_new_user();

create table if not exists public.admin_users (
  user_id uuid primary key references auth.users(id) on delete cascade,
  role text not null default 'admin' check (role in ('admin', 'super_admin')),
  created_at timestamptz not null default now()
);

create table if not exists public.visitor_events (
  id bigint generated always as identity primary key,
  event_type text not null default 'page_view' check (event_type = 'page_view'),
  page_path text not null check (char_length(page_path) between 1 and 200),
  session_id uuid not null,
  poem_id integer references public.poems(id) on delete set null,
  referrer_host text,
  device_type text not null check (device_type in ('desktop', 'tablet', 'mobile')),
  created_at timestamptz not null default now()
);
create index if not exists visitor_events_created_idx on public.visitor_events(created_at desc);
create index if not exists visitor_events_session_created_idx on public.visitor_events(session_id, created_at desc);
create index if not exists visitor_events_poem_created_idx on public.visitor_events(poem_id, created_at desc);

create or replace function public.is_admin()
returns boolean language sql stable security definer set search_path = ''
as $$ select exists(select 1 from public.admin_users where user_id = auth.uid()) $$;

alter table public.poems enable row level security;
alter table public.admin_users enable row level security;
alter table public.visitor_events enable row level security;

drop policy if exists "public read published poems" on public.poems;
create policy "public read published poems" on public.poems for select
using (is_published = true);
drop policy if exists "admins manage poems" on public.poems;
create policy "admins manage poems" on public.poems for all
using (public.is_admin()) with check (public.is_admin());

drop policy if exists "admins read own membership" on public.admin_users;
create policy "admins read own membership" on public.admin_users for select
using (user_id = auth.uid());

alter table public.user_profiles enable row level security;
alter table public.favorites enable row level security;
